# Latihan Operasi Aritmatika

nilai = float(input("Masukkan nilai suhu dalam Celsius: "))

# Konversi ke Fahrenheit
fahrenheit = (nilai * 9/5) + 32
print("Nilai dari Fahrenheit = ", fahrenheit)

# Konversi ke Kelvin
kelvin = nilai + 273.15  # Gunakan titik bukan koma
print("Nilai dari Kelvin = ", kelvin)

# Konversi ke Reamur
reamur = (nilai * 4/5)
print("Nilai dari Reamur = ", reamur)